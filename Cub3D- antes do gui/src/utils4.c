int	strlen_special(char *s, int i)
{
	while (s[i] && s[i] == ' ')
		i++;
	return (i);
}

int	strlen_special2(char *s)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != '\n')
		i++;
	i--;
	while (s[i] == ' ')
		i--;
	return (i);
}
